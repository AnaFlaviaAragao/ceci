#ifndef SUM_H
#define SUM_H
/**
 * Sum module
 * 
 * @param a First value
 * @param b Second value
 * @return Sum of both above values
 */
int soma(int a, int b, int ci);

/**
 * Carry Out value for the Sum
 * 
 * @param a First value
 * @param b Second value
 * @return Carry Out value for the above values sum
 */
int carry(int a, int b, int ci);
#endif //SUM_H